	function ln_gamma(z) result(f)
! ln_gamma  Computes the logarithm of the Gamma function in the entire complex plane to relative accuracy 10^{-33} 
!
!   f = ln_gamma(z) returns log(Gamma(z)) for any complex input z.
!   The input z has to be a scalar (this code is not vectorized)
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ 
! Author: Alexey Kuznetsov
! York University, Toronto, Canada
! Website: https://kuznetsovmath.ca/
! Email: akuznets@yorku.ca
!
! Created: 28-Nov-2025
! Last updated: 2-April-2026
!
! License: BSD 3-Clause (https://opensource.org/licenses/BSD-3-Clause)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ 		
	implicit none
	complex(kind=16), intent(in)	:: z
	complex(kind=16)		:: f
	real(kind=16)			:: d
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  real(kind=16), parameter	:: A=0.91893853320467274178032973640561763986q0 ! precomputed value of (1/2)*log(2*pi)
  complex(kind=16), parameter	:: pi_i=(0.0q0,3.141592653589793238462643383279502884q0) ! precomputed value of pi*i
  	if (z%re>=0.5q0) then
  		f=ln_gamma_half_plane(z)
  	else   ! use reflection formula
  		d=sign(1.0q0,z%im)
  		f=2*A-ln_gamma_half_plane(1-z)+d*pi_i*(z-0.5q0)
  		if (abs(z%im)<13.0q0) f=f-log(1-exp(2*d*pi_i*z))
  	end if 		
  	end function ln_gamma	
