module Riemann_zeta_module
	implicit none
	real(kind=16), parameter     :: pi_16=3.141592653589793238462643383279502884q0
	complex(kind=16), parameter  :: i_16=(0.0q0,1.0q0) 
contains
!#######################################################################
	function Riemann_zeta(s) result(f)
!--------------------------------------------------------------------------	
	implicit none
	complex (kind=16), intent(in)	:: s	
	complex (kind=16)		:: f	
	real (kind=16)			:: d
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	if (s%re>=0.5q0) then
		f=Riemann_zeta_half_plane(s)
	elseif (abs(s)<5.0q-3) then ! use Taylor series if |s| is small
		f=-0.5q0+s*(-0.91893853320467274178032973640561q0+s*(-1.0031782279542924256050500134q0&
 		+s*(-1.0007851944770424079602q0+s*(-0.99987929950057116q0+s*(-1.000001940896q0+s*(-1.0000013011q0-s))))))	
	else ! use reflection formula for the Riemann zeta function for Re(s)<0.5
		d=sign(1.0q0,s%im)
		f=d*i_16*(2*pi_16)**(s-1)*exp(-d*0.5q0*i_16*pi_16*s+ln_gamma(1-s))*Riemann_zeta_half_plane(1-s)
		if (abs(s%im)<15.0q0) f=f*(1-exp(d*i_16*pi_16*s))
	end if	
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~	
	end function Riemann_zeta
!#######################################################################
	function Riemann_zeta_half_plane(s) result(f)
! computes zeta(s) in the half-plane Re(s)>=0.5
	implicit none
	complex (kind=16), intent(in)	:: s	
	complex (kind=16)		:: f
	real(kind=16)			:: N_sum, N_EM, N_12
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~	
	N_sum=8*exp(45/max(s%re-1,0.5q0))/30  ! computational complexity of zeta_summation function -- counting the number of evaluations of k^{-s}
	N_EM=abs(s)/4+5			      ! computational complexity of zeta_Euler_Maclaurin function			
	N_12=sqrt(abs(s%im)/(2*pi_16))/4+50   ! computational complexity of zeta_12 function
	if (abs(s%im)<200) then 
		if (N_sum<N_EM) then
			f=zeta_summation(s)
		else
			f=zeta_Euler_Maclaurin(s)
		end if
	elseif (N_sum<N_12) then 
		f=zeta_summation(s)	
	else
		if (s%im>0) then
			f=zeta_12(s)
		else
			f=conjg(zeta_12(conjg(s)))
		end if				
	end if	
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~	
	end function Riemann_zeta_half_plane
!#######################################################################	
	function zeta_summation(s) result(f)
! approximates the Riemann zeta function f=zeta(s)=\sum_{n=1}^{\infty} n^{-s} 	
! to have more efficient computation, we instead compute g(s)=((1-2^{-s})(1-3^{-s})(1-5^{-s})) zeta(s) 
! which is the Dirichlet series g(z)=\sum_{n>=1, gcd(n,30)=1} n^{-s} 
! this trick removes 11/15 (or 73.3%) of the terms from the original sum \sum_{n=1}^{\infty} n^{-s}
! we truncate the Dirichlet series for g(z) at n=N*30
! for Re(s)>2 the error is smaller than (30*N)^(1-Re(s))
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~	
	implicit none
	complex (kind=16), intent(in)	:: s
	complex (kind=16)		:: f
	integer, parameter 		:: res30(1:8)=(/1, 7, 11, 13, 17, 19, 23, 29/) ! residue classes mod 30 that are co-prime to 30
	integer				:: N, j, l
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	N=floor(exp(45/(s%re-1))/30)  ! choose N so that the error is smaller than 10^{-19}
	f=0
	do j=1,8
		do l=0,N
			f=f+(res30(j)+30*l)**(-s)
		end do
	end do
	f=f/((1-2**(-s))*(1-3**(-s))*(1-5**(-s)))
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	end function zeta_summation
!#######################################################################	
	function RS_main_sum(s,x) result(f)
! This function computes f(1)=\sum_{1\le n \le x} n^{-s} and f(2)=\sum_{1\le n \le x} n^{s-1}.
! We reduce the number of evaluations of the exponential function n^{-s} 
! by using factorization n=m 2^a 3^b 5^c, where m is coprime to 30.
! The algorithm evaluates n^{-s} only for n=2,3,5 and for integers m coprime to 30,
! and generates all multiples of m by repeated multiplication by 2, 3, and 5.
! The number of exponential function evaluations is decreased by 73.3%,
! compared with the simple summation approach to compute f(1) and f(2). 
! This optimization allows to compute zeta(s) faster when Im(s) is very large.
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
   	implicit none
   	integer, intent(in)		:: x
   	complex(kind=16), intent(in)	:: s
   	integer, parameter 		:: res30(1:8) = (/1, 7, 11, 13, 17, 19, 23, 29/)
	integer 			:: b, r, m, n2, n3, n5, x2, x3, x5
	complex(kind=16) 		:: u2, u3, u5, v2, v3, v5, t0, t2, t3, t5
	complex(kind=16)		:: g0, g2, g3, g5, sum1, sum2, f(1:2)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	u2=2**(-s); u3=3**(-s); u5=5**(-s)
	v2=1/(2*u2); v3=1/(3*u3); v5=1/(5*u5)
	x2=x/2; x3=x/3; x5=x/5
	sum1=0.0q0
	sum2=0.0q0
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	do b=0,((x-1)/30)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        do r=1,8
		m=30*b+res30(r)
		if (m>x) exit
		if (m==1) then
			t0=1.0q0
			g0=1.0q0
		else
			t0=m**(-s)
			g0=1.0q0/(m*t0)
		end if
		n2=m; t2=t0; g2=g0
		do
			n3=n2; t3=t2; g3=g2
			do
				n5=n3; t5=t3; g5=g3
                    do
					sum1=sum1+t5
					sum2=sum2+g5
					if (n5>x5) exit
					n5=5*n5; t5=t5*u5; g5=g5*v5
 				end do
				if (n3>x3) exit
				n3=3*n3; t3=t3*u3; g3=g3*v3 
			end do
			if (n2>x2) exit
			n2=2*n2; t2=t2*u2; g2=g2*v2
		end do
	end do
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~	
	end do
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	f(1)=sum1
	f(2)=sum2
	end function RS_main_sum
!#######################################################################
	function zeta_Euler_Maclaurin(s) result(f)
	implicit none
	complex (kind=16), intent(in)	:: s
	complex (kind=16)		:: f, m, g(1:2)
	integer				:: k, N
	real (kind=16)			:: N2
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
! computes zeta(s) via Euler-Maclaurin summation 
! we use formula (25.2.10) at https://dlmf.nist.gov/25.2 with n=25
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
! precompute b(k)=B_{2k}/(2k)!, where B_{k} are Bernoulli numbers
   real(kind=16), parameter	:: b(1:25)=&	
   (/8.33333333333333333333333333333333293q-2,&
    -1.38888888888888888888888888888888893q-3,&
    3.30687830687830687830687830687830844q-5,&
    -8.26719576719576719576719576719577148q-7,&
    2.08767569878680989792100903212014440q-8,&
    -5.28419013868749318484768220217955963q-10,&
    1.33825365306846788328269809751291339q-11,&
    -3.38968029632258286683019539124944525q-13,&
    8.58606205627784456413590545042563573q-15,&
    -2.17486869855806187304151642386592068q-16,&
    5.50900282836022951520265260890226040q-18,&
    -1.39544646858125233407076862640635689q-19,&
    3.53470703962946747169322997780380424q-21,&
    -8.95351742703754685040261131811275565q-23,&
    2.26795245233768306031095073886817000q-24,&
    -5.74479066887220244526388198760702843q-26,&
    1.45517247561486490186626486727133221q-27,&
    -3.68599494066531017818178247990866798q-29,&
    9.33673425709504467203255515278564467q-31,&
    -2.36502241570062993455963519636984353q-32,&
    5.99067176248213430465991239681967181q-34,&
    -1.51745488446829026171081313586472285q-35,&
    3.84375812545418823222944529099024291q-37,&
    -9.73635307264669103526762127925048019q-39,&
    2.46624704420068095710640028028884984q-40/)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ 
	N=floor(abs(s))+20
	N2=1.0q0/N**2	
	m=s/N
	f=0
	do k=1,25
		f=f+b(k)*m
		m=m*(s+2*k-1)*(s+2*k)*N2
	end do
	f=N**(-s)*(f+0.5q0+N/(s-1))
	g=RS_main_sum(s,N-1)
	f=f+g(1)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~  
	end function zeta_Euler_Maclaurin
